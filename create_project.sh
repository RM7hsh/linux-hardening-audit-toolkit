write_file lhat/runner.py <<'EOF'
import json
from pathlib import Path

from lhat.checks import disk, firewall, ports, services, ssh, sudo, updates, users
from lhat.models import CheckResult


CHECK_MODULES = [
    ssh,
    firewall,
    users,
    sudo,
    services,
    ports,
    updates,
    disk,
]


def run_all_checks(root: Path = Path("/")) -> list[CheckResult]:
    results: list[CheckResult] = []
    for module in CHECK_MODULES:
        results.extend(module.run(root=root))
    return results


def to_json(results: list[CheckResult]) -> str:
    return json.dumps([result.__dict__ for result in results], indent=2, ensure_ascii=False)


def to_markdown(results: list[CheckResult]) -> str:
    lines = ["# Linux Hardening Audit Report", ""]
    lines.append("| Check | Status | Message |")
    lines.append("|---|---|---|")
    for result in results:
        lines.append(f"| {result.check_id} | {result.status} | {result.message} |")
    return "\n".join(lines) + "\n"
EOF

write_file lhat/cli.py <<'EOF'
import argparse
from pathlib import Path

from lhat.runner import run_all_checks, to_json, to_markdown


def build_parser() -> argparse.ArgumentParser:
    parser = argparse.ArgumentParser(description="Linux hardening audit toolkit")
    parser.add_argument("--root", default="/", help="Root directory to inspect")
    parser.add_argument("--format", choices=["json", "markdown"], default="markdown")
    parser.add_argument("--output", help="Write report to file")
    return parser


def main() -> None:
    args = build_parser().parse_args()
    results = run_all_checks(root=Path(args.root))
    output = to_json(results) if args.format == "json" else to_markdown(results)
    if args.output:
        Path(args.output).write_text(output, encoding="utf-8")
    else:
        print(output)


if __name__ == "__main__":
    main()
EOF

