write_file lhat/shell.py <<'EOF'
import subprocess


def run_command(command: list[str], timeout: int = 5) -> tuple[int, str, str]:
    try:
        completed = subprocess.run(
            command,
            capture_output=True,
            text=True,
            timeout=timeout,
            check=False,
        )
        return completed.returncode, completed.stdout.strip(), completed.stderr.strip()
    except FileNotFoundError:
        return 127, "", f"command not found: {command[0]}"
    except subprocess.TimeoutExpired:
        return 124, "", "command timed out"
EOF

write_file lhat/live.py <<'EOF'
from lhat.models import CheckResult, Status
from lhat.shell import run_command


def collect_open_ports() -> CheckResult:
    code, stdout, stderr = run_command(["ss", "-tulpen"])
    if code == 0:
        lines = len(stdout.splitlines())
        return CheckResult("LIVE-PORTS", "Live open ports", Status.INFO, f"ss returned {lines} lines")
    return CheckResult("LIVE-PORTS", "Live open ports", Status.WARN, stderr or "ss command failed")


def collect_failed_units() -> CheckResult:
    code, stdout, stderr = run_command(["systemctl", "--failed", "--no-pager"])
    if code == 0:
        return CheckResult("LIVE-SYSTEMD", "Failed systemd units", Status.INFO, stdout or "No output")
    return CheckResult("LIVE-SYSTEMD", "Failed systemd units", Status.WARN, stderr or "systemctl failed")
EOF

